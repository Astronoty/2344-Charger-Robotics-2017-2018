#define PID_INTEGRAL_LIMIT  50
#define PID_DRIVE_MAX       127
#define PID_DRIVE_MIN     (-127)
#define CYCLE_TIME 					20


static float pidRequestedValueLeft, pidRequestedValueRight;
static float Kp = 1;
static float Ki = 0;
static float Kd = 0;
static float leftDriveScale = 0.6; //Multiply faster motor by this so speed is equal with slower motor
static float rightDriveScale = 1;
static bool pidActive = true;


static float velErrorKp = .1;

bool matchSideVelocities = true;

