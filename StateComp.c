#define PID_INTEGRAL_LIMIT_ELEVATOR  50

static float pidRequestedElevator;
static float elevatorKp = 1;
static float elevatorKi = 0;
static float elevatorKd = 0;
static bool pidLift = false;


task liftControl()
{
	float  currentElevatorPos;
	float  pidElevatorError;
	float  pidLastElevatorError;
	float  pidElevatorIntegral;
	float  pidElevatorDerivative;
	float  pidElevatorDrive;
	while(true)
	{
		if(pidLift)
		{
			currentElevatorPos = SensorValue[elevator];

			pidElevatorError = currentElevatorPos - pidRequestedElevator;

			// Calculate integral portion
			// Add the error to the integral unless the error bound grows too large
			if(abs(pidElevatorError) < PID_INTEGRAL_LIMIT_ELEVATOR)
				pidElevatorIntegral = pidElevatorIntegral + pidElevatorError;
			else
				pidElevatorIntegral = 0;


			//Calculate derivitave portion
			pidElevatorDerivative = pidElevatorError - pidLastElevatorError;

			pidLastElevatorError = pidElevatorError;


			//Calculate drive
			pidElevatorDrive = (elevatorKp * pidElevatorError) + (elevatorKi * pidElevatorIntegral) + (elevatorKd * pidElevatorDerivative);



			//Scale down to proper motor values
			if( pidElevatorDrive > PID_DRIVE_MAX )
				pidElevatorDrive = PID_DRIVE_MAX;
			if( pidElevatorDrive < PID_DRIVE_MIN )
				pidElevatorDrive = PID_DRIVE_MIN;


			//Drive using PID commands
			motor[elevatorOne] = (int)pidElevatorDrive;


			wait1Msec(CYCLE_TIME);

		}

	}
}

