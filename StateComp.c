task pidController()
{
	//Initialize PID variables, only using P for now
	float  pidSensorCurrentValueLeft, pidSensorCurrentValueRight;
	float  pidErrorLeft, pidErrorRight;
	float  pidLastErrorLeft, pidLastErrorRight;
	float  pidIntegralLeft, pidIntegralRight;
	float  pidDerivativeLeft, pidDerivativeRight;
	float  pidDriveLeft, pidDriveRight;

	float velDiffError;


	//Clear sensor values
	SensorValue[rightEnc] = 0;
	SensorValue[leftEnc] = 0;

	pidLastErrorLeft = 0;
	pidLastErrorRight = 0;
	pidIntegralLeft = 0;
	pidIntegralRight = 0;

	velDiffError = 0;
	while(true)
	{
		//If the pid is set to active
		if(pidActive)
		{
			//Calculate proportional portion
			pidSensorCurrentValueLeft = SensorValue[leftEnc];
			pidSensorCurrentValueRight = SensorValue[rightEnc];

			pidErrorLeft = pidSensorCurrentValueLeft - pidRequestedValueLeft;
			pidErrorRight = pidSensorCurrentValueRight - pidRequestedValueRight;


			// Calculate integral portion
			// Add the error to the integral unless the error bound grows too large
			if(abs(pidErrorLeft) < PID_INTEGRAL_LIMIT)
				pidIntegralLeft = pidIntegralLeft + pidErrorLeft;
			else
				pidIntegralLeft = 0;

			if(abs(pidErrorRight) < PID_INTEGRAL_LIMIT)
				pidIntegralRight = pidIntegralRight + pidErrorRight;
			else
				pidIntegralRight = 0;

			//Calculate derivitave portion
			pidDerivativeLeft = pidErrorLeft - pidLastErrorLeft;
			pidDerivativeRight = pidErrorRight - pidLastErrorRight;

			pidLastErrorLeft  = pidErrorLeft;
			pidLastErrorRight = pidErrorRight;

			//Compensate for one side moving faster
			velDiffError = pidSensorCurrentValueLeft - pidSensorCurrentValueRight;

			writeDebugStreamLine("LeftEnc: %f", pidSensorCurrentValueLeft);
			writeDebugStreamLine("RightEnc: %f", pidSensorCurrentValueRight);


			//Calculate drive
			pidDriveLeft = (Kp * pidErrorLeft) + (Ki * pidIntegralLeft) + (Kd * pidDerivativeLeft);
			pidDriveRight = (Kp * pidErrorRight) + (Ki * pidIntegralRight) + (Kd * pidDerivativeRight);



			//Scale down to 127, -127 so drive distance gains do not override corrective vel matching
			if( pidDriveLeft > PID_DRIVE_MAX )
				pidDriveLeft = PID_DRIVE_MAX;
			if( pidDriveLeft < PID_DRIVE_MIN )
				pidDriveLeft = PID_DRIVE_MIN;

			if( pidDriveRight > PID_DRIVE_MAX )
				pidDriveRight = PID_DRIVE_MAX;
			if( pidDriveRight < PID_DRIVE_MIN )
				pidDriveRight = PID_DRIVE_MIN;

					writeDebugStreamLine("DriveBeforeCorrectLeft: %f", pidDriveLeft);
				writeDebugStreamLine("DriveBeforeCorrectRight: %f", pidDriveRight);


		  //Recalculate drive with corrective vel matching on each side
			pidDriveLeft = pidDriveLeft + (velErrorKp * velDiffError);
			pidDriveRight = pidDriveRight - (velErrorKp * velDiffError);

			writeDebugStreamLine("Vel Error: %f", velDiffError);

			writeDebugStreamLine("DriveLeft After: %f", pidDriveLeft);
			writeDebugStreamLine("DriveRight After: %f", pidDriveRight);


			//Prevent setting over valid motor values once more
			if( pidDriveLeft > PID_DRIVE_MAX )
				pidDriveLeft = PID_DRIVE_MAX;
			if( pidDriveLeft < PID_DRIVE_MIN )
				pidDriveLeft = PID_DRIVE_MIN;

			if( pidDriveRight > PID_DRIVE_MAX )
				pidDriveRight = PID_DRIVE_MAX;
			if( pidDriveRight < PID_DRIVE_MIN )
				pidDriveRight = PID_DRIVE_MIN;

			writeDebugStreamLine("Final Left : %f", pidDriveLeft);
			writeDebugStreamLine("Final Right : %f", pidDriveRight);


			//Drive using PID commands
			motor[driveLeftOne] = (int)((float)pidDriveLeft * (-1.0)*leftDriveScale);
			motor[driveLeftTwo] = (int)((float)pidDriveLeft * (-1.0)*leftDriveScale);
			motor[driveLeftThree] = (int)((float)pidDriveLeft * (-1.0)*leftDriveScale);
			motor[driveLeftFour] = (int)((float)pidDriveLeft * (-1.0)*leftDriveScale);
			motor[driveRightOne] = (int)((float)pidDriveRight * (-1.0)*rightDriveScale);
			motor[driveRightTwo] = (int)((float)pidDriveRight * (-1.0)*rightDriveScale);
			motor[driveRightThree] = (int)((float)pidDriveRight * (-1.0)*rightDriveScale);
			motor[driveRightFour] = (int)((float)pidDriveRight * (-1.0)*rightDriveScale);

			wait1Msec(CYCLE_TIME);
		}
		else //delete the emails, i mean variables
		{
			pidSensorCurrentValueLeft = 0;
			pidSensorCurrentValueRight = 0;
			pidErrorLeft = 0;
			pidErrorRight = 0;
			pidLastErrorLeft = 0;
			pidLastErrorRight = 0;
			pidIntegralLeft = 0;
			pidIntegralRight = 0;
			pidDerivativeLeft = 0;
			pidIntegralRight = 0;
			pidDriveLeft= 0;
			pidDriveRight = 0;
			motor[driveLeftOne] = 0;
			motor[driveLeftTwo] = 0;
			motor[driveLeftThree] = 0;
			motor[driveLeftFour] = 0;
			motor[driveRightOne] = 0;
			motor[driveRightTwo] = 0;
			motor[driveRightThree] = 0;
			motor[driveRightFour] = 0;
		}

	}
}




